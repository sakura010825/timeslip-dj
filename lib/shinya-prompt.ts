type SeasonKey = 'spring' | 'summer' | 'autumn' | 'winter';

const SEASON_LABEL: Record<SeasonKey, string> = {
  spring: '春',
  summer: '夏',
  autumn: '秋',
  winter: '冬',
};

const SEASON_MONTHS_LABEL: Record<SeasonKey, string> = {
  spring: '3〜5月',
  summer: '6〜8月',
  autumn: '9〜11月',
  winter: '12〜2月',
};

const CATEGORY_LABEL: Record<string, string> = {
  news: 'ニュース',
  music: '音楽',
  tv: 'テレビ',
  movie: '映画',
  'cm-ads': 'CM・広告',
  trend: '流行・ブーム',
  'tech-gadget': '技術・ガジェット',
  food: '食・外食',
  sports: 'スポーツ',
  slang: 'スラング・若者文化',
};

export function seasonLabel(season: string): string {
  return SEASON_LABEL[season as SeasonKey] ?? season;
}

export function buildScriptPrompt(params: {
  year: number;
  season: string;
  topics: KnowledgeItem[];
  musicPool?: KnowledgeItem[];
}): string {
  const { year, season, topics, musicPool } = params;
  const sLabel = seasonLabel(season);
  const monthsLabel = SEASON_MONTHS_LABEL[season as SeasonKey] ?? '';

  const topicsBlock = topics
    .map(
      (t, i) => `
【素材${i + 1}】${t.title}（${t.month}月・${CATEGORY_LABEL[t.category] ?? t.category}・重要度${t.importance}）
- 事実: ${t.oneLiner}
- 文脈: ${t.context}
- キーワード: ${t.keywords.join('、')}`
    )
    .join('\n');

  // 楽曲候補ブロック: 知識ベースの music カテゴリ全件を提示し、
  // Claude が史実外の曲を hallucination することを防ぐ。
  const musicBlock = musicPool && musicPool.length > 0
    ? `

【楽曲候補リスト（必ずこのリストから3曲選ぶこと）】
${musicPool
  .map(
    (m, i) =>
      `${i + 1}. ${m.title}
   事実: ${m.oneLiner}
   キーワード: ${m.keywords.join('、')}`,
  )
  .join('\n\n')}

⚠️ 楽曲選定の絶対ルール:
- 上記の候補リストから **3曲** を選んで番組を構成してください
- 候補が3曲未満の場合は、利用可能な曲だけで構成し、楽曲数を減らしても構いません
- 候補が3曲以上ある場合は、話の流れに合うものを選んでください
- 候補リスト**以外**の楽曲を選ぶことは禁止です。たとえ${year}年${sLabel}にヒットしていても、リストにない曲は使用しないでください（史実誤認・アーカイブ重複防止のためのルール）
- 出力JSONの songTitle / artistName は、候補の楽曲名・アーティスト名を正確に再現してください`
    : '';

  return `${year}年${sLabel}（${monthsLabel}）の30分番組の台本を、シンヤの声で書いてください。

【今夜の素材（5項目）】
以下のトピックを材料にしてください。全てを均等に扱う必要はありません。1エピソードの中で自然に消化できる範囲で、素材の「文脈」を自分の言葉で語ってください。
${topicsBlock}${musicBlock}

【セグメント構成（合計約28〜29分）】
1. オープニング（約3分・約800〜1000文字）
   - 「……こんばんは。シンヤです。」から始める
   - ${year}年${sLabel}の時代の空気を、素材のうち1〜2項目を入口にして導入
   - この夜のリスナーに会えた静かな喜びを、控えめに
   - 最後に楽曲1を紹介して、「シートベルトを、どうぞ。」で締める

2. ミドルトーク1（約5分・約1300〜1600文字）
   - テーマ: 「ニュース・時代の空気」
   - 素材の中から「時代の空気」が伝わる項目を1〜2つ、深く語る（news/sports/trend/cm-adsが入っていれば優先）
   - シンヤが立ち会った一人の目撃者として。感情を込めすぎず、でも体温は残して
   - 最後に楽曲2を紹介

3. ミドルトーク2（約5分・約1300〜1600文字）
   - テーマ: 「時代の深掘りコーナー」
   - 素材から1〜2項目、少し違う角度（文化・技術・流行・サブカル）で深掘り（tech-gadget/tv/movie/slangが入っていれば優先）
   - 「今の若い人には伝わらないかもしれない」角度の話を、押し付けなしに
   - 最後に楽曲3を紹介

4. エンディング（約3〜4分・約800〜1000文字）
   - 今夜触れたトピックをひとつ、軽く振り返る
   - 余韻のある、静かな締めくくり
   - 最後に「シートベルトを外して、どうか、あなたの『今』にお戻りください。」

【楽曲選定】
- 各楽曲は${year}年${sLabel}にヒットしていた、または時代を象徴する日本の楽曲から選んでください
- 楽曲は3曲（オープニング、ミドル1後、ミドル2後）。エンディングには楽曲は付きません
- 楽曲の紹介は短くていい。流れを殺さない長さで

【出力形式（必ずこのJSONのみ、説明やコメント不要）】
{
  "segments": [
    {
      "segmentTitle": "オープニング",
      "script": "台本内容...",
      "songTitle": "曲名",
      "artistName": "アーティスト名"
    },
    {
      "segmentTitle": "ミドルトーク1",
      "script": "...",
      "songTitle": "...",
      "artistName": "..."
    },
    {
      "segmentTitle": "ミドルトーク2",
      "script": "...",
      "songTitle": "...",
      "artistName": "..."
    },
    {
      "segmentTitle": "エンディング",
      "script": "...",
      "songTitle": null,
      "artistName": null
    }
  ]
}

【TTS読み上げ精度のためのルール（scriptフィールド内でのみ適用・重要）】

■ 英語・アルファベット略語
- 必ずカタカナで書く（例: ピーエイチエス、シーディー、テレビ、オーエス、ジェイポップ、ビデオデッキ）
- 英単語はそのまま使わず、カタカナか日本語に置き換える

■ 数字の扱い（誤読防止のため厳守）
- 年号は算用数字で書く
  正: 「1995年」「2000年」「1990年代」
  禁止: 「一九九五年」（漢数字にすると「いち きゅう きゅう ご」と桁読みされる）
- 月・日・曜日・時刻は漢数字でもよい
  例: 「十月」「十一月二十三日」「午前三時」
- 数値を含む固有名詞・ブランド名・暗号などは、必ず読み仮名カタカナに置き換える
  例: 「109」→「イチマルキュウ」（渋谷の商業ビル）
  例: 「14106」→「イチヨンイチマルロク」（ポケベル暗号）
  例: 「Windows 95」→「ウィンドウズナインティファイブ」
  例: 「B'z」→「ビーズ」
  例: 「Mr.Children」→「ミスターチルドレン」
  ※ songTitle / artistName フィールドは正式表記のまま。script本文だけカタカナ化する
- 年齢・人数などの量を表す数字は算用数字でも漢数字でも可
  例: 「50代」または「五十代」

■ 記号
- 「※」「→」「…」「『』の中の「」」などは使わない
- 読み上げづらい記号は、読める日本語で表現する

■ その他
- 読みにくい漢字や熟語は、ひらがなに開く

【固有名詞の例外】
- 上記ルールは script フィールドの中だけに適用します
- songTitle / artistName は YouTube 検索に使うので、正式表記（英字・漢字含む）のまま出力してください
  例: songTitle: "LOVE PHANTOM" / artistName: "B'z"
  例: songTitle: "TRY ME 〜私を信じて〜" / artistName: "安室奈美恵"
- ただし script の本文でアーティスト名を読み上げる箇所は、カタカナで書いてください（例: ビーズ、アムロナミエ）`;
}
